    private void crearPedido(String documentoCliente) {
        try {
            Date fechaActual = new Date();
            String documentoCajero = "100001";

            CallableStatement crearPedidoStmt = (CallableStatement) Conexion.conexion.prepareCall("{CALL CrearPedido(?, ?, ?, ?, ?, ?)}");
            crearPedidoStmt.setDate(1, new java.sql.Date(fechaActual.getTime()));
            crearPedidoStmt.setString(2, "En Proceso");
            crearPedidoStmt.setDouble(3, 0.0);
            crearPedidoStmt.setString(4, "");
            crearPedidoStmt.setString(5, documentoCajero);
            crearPedidoStmt.setString(6, documentoCliente);
            crearPedidoStmt.execute();

            ultimoPedidoId = obtenerUltimoPedidoId();
            CallableStatement actualizarSubtotalStmt = (CallableStatement) Conexion.conexion.prepareCall("{CALL ActualizarSubTotal(?)}");
            actualizarSubtotalStmt.setInt(1, ultimoPedidoId);
            actualizarSubtotalStmt.execute();
            JOptionPane.showMessageDialog(this, "Pedido creado exitosamente", "Ãxito", JOptionPane.INFORMATION_MESSAGE);

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    public static int obtenerUltimoPedidoId() {
        int ultimoPedidoId = -1;
        try {
            String query = "SELECT LAST_INSERT_ID() AS last_id";
            PreparedStatement preparedStatement = Conexion.conexion.prepareStatement(query);
            ResultSet resultSet = preparedStatement.executeQuery(query);
            if (resultSet.next()) {
                ultimoPedidoId = resultSet.getInt("last_id");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        return ultimoPedidoId;
    }


