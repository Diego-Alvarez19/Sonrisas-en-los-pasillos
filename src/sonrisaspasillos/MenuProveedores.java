     private void obtenerDatosProveedor(int codigoProducto) {
        try {
            // Llama al procedimiento almacenado
            CallableStatement obtenerDatosStmt = (CallableStatement) Conexion.conexion.prepareCall("{CALL ObtenerDatosProveedor(?)}");
            obtenerDatosStmt.setInt(1, codigoProducto);

            // Ejecuta el procedimiento almacenado y obtiene el resultado
            ResultSet resultSet = obtenerDatosStmt.executeQuery();

            // Verifica si hay resultados antes de actualizar los JLabel
            if (resultSet.next()) {
                // Obtiene los datos del proveedor desde el resultado
                String nombreEmpresa = resultSet.getString("Nombre_Empresa");
                String contacto = resultSet.getString("Numero_Contacto");
                String celular = resultSet.getString("Celular");
                String correo = resultSet.getString("Correo_Proveedores");

                // Actualiza los JLabel con la información del proveedor
                txt_empresa.setText("Empresa: " + nombreEmpresa);
                txt_contacto.setText("Contacto: " + contacto);
                txt_celular.setText("Celular: " + celular);
                txt_correo.setText("Correo: " + correo);
            } else {
                // Si no hay resultados, puedes mostrar un mensaje o realizar otra acción
                JOptionPane.showMessageDialog(this, "No se encontraron datos para el producto con código " + codigoProducto, "Información", JOptionPane.INFORMATION_MESSAGE);
            }

        } catch (SQLException e) {
            // Manejo de excepciones de SQL
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error al obtener datos del proveedor", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }


