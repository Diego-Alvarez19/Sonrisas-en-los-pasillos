    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
        // Obtén el código de producto desde el campo de texto
        int codigoProducto = Integer.parseInt(txt_codigo.getText());

        // Llama al procedimiento almacenado
        CallableStatement obtenerDatosStmt = (CallableStatement) Conexion.conexion.prepareCall("{CALL ObtenerDatosProveedor(?)}");
        obtenerDatosStmt.setInt(1, codigoProducto);

        // Ejecuta el procedimiento almacenado y obtén el conjunto de resultados
        ResultSet resultSet = obtenerDatosStmt.executeQuery();

        // Verifica si hay resultados antes de intentar acceder a ellos
        if (resultSet.next()) {
            // Obtén los datos utilizando los nombres de las columnas exactos
            String nombreEmpresa = resultSet.getString("Nombre_Empresa");
            String nombreContacto = resultSet.getString("Nombre_Contacto");
            String numeroContacto = resultSet.getString("Numero_Contacto");
            String correoProveedor = resultSet.getString("Correo_Proveedores");

            // Asigna los datos a las etiquetas en tu interfaz gráfica
            txt_empresa.setText(nombreEmpresa);
            txt_contacto.setText(nombreContacto);
            txt_celular.setText(numeroContacto);
            txt_correo.setText(correoProveedor);
        } else {
            // Manejar el caso en el que no hay resultados
            JOptionPane.showMessageDialog(this, "No se encontraron resultados para el código de producto: " + codigoProducto, "Error", JOptionPane.ERROR_MESSAGE);
        }

    } catch (NumberFormatException e) {
        // Manejar el caso en el que el texto ingresado no es un número
        JOptionPane.showMessageDialog(this, "Ingrese un código de producto válido", "Error", JOptionPane.ERROR_MESSAGE);
    } catch (SQLException e) {
        // Manejo de excepciones de SQL
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error al ejecutar el procedimiento almacenado", "Error", JOptionPane.ERROR_MESSAGE);
    }
        
    }//GEN-LAST:event_jButton3ActionPerformed

